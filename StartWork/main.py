# 3. Напишите программу, которая будет выводить одно сообщение, если значение переменной меньше или равно 10, другое, если переменная больше 10,
# но меньше или равна 25, и третье сообщение, если переменная больше 25.

x = float(input("Введите значение: "))

if x <= 10:
    print("Значение меньше или равно 10")
elif x <= 25: # Здесь условие неявно означает: 10 < x <= 25
    print("Значение больше 10, но меньше или равна 25")
else: # x > 25
    print("Значение больше 25")

# 4. Напишите программу, которая выполняет деление двух чисел и выводит остаток.

# Запрашиваем два числа у пользователя
dividend = float(input("Введите делимое: "))
divisor = float(input("Введите делитель: "))

# Проверяем, что делитель не равен нулю
if divisor == 0:
    print("Ошибка: деление на ноль невозможно!")
else:
    # Вычисляем остаток от деления
    remainder = dividend % divisor
    print(f"Остаток от деления {dividend} на {divisor} равен {remainder}")

# 5. Напишите программу, которая принимает две переменные, делит одну на другую и выводит частное.

# Запрашиваем два числа у пользователя
dividend = float(input("Введите делимое: "))
divisor = float(input("Введите делитель: "))

# Проверяем, что делитель не равен нулю
if divisor == 0:
    print("Ошибка: деление на ноль невозможно!")
else:
    # Вычисляем частное
    quotient = dividend / divisor
    print(f"Частное от деления {dividend} на {divisor} равно {quotient}")

