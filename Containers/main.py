# Поиск подозрительных IP (которые были более 3 раз)
from collections import Counter
ip_count = Counter(ip_addresses)
suspicious_ips = {ip for ip, count in ip_count.items() if count > 3}
print(f"Подозрительные IP: {suspicious_ips}")