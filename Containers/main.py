# 4. Напишите программу, которая запрашивает у пользователя его вес, любимый цвет или актер,
# и возвращает результат из словаря, созданного в предыдущем задании.

person_info = {
    "вес": 72,
    "любимый_цвет": "синий",
    "любимый_актер": "Леонардо ДиКаприо",
    "рост": 175,
    "возраст": 28,
    "хобби": "плавание",
    "любимый_фильм": "Начало"
}


def interactive_search():
    """
    Интерактивная программа для поиска информации.
    """
    print("=" * 50)
    print("     ПРОГРАММА ПОИСКА ИНФОРМАЦИИ")
    print("=" * 50)

    while True:
        print("\nЧто вы хотите узнать?")
        print("Доступные категории:")

        # Показываем доступные категории
        for i, key in enumerate(person_info.keys(), 1):
            print(f"  {i}. {key}")

        print("\nили введите 'выход' для завершения программы")

        # Получаем запрос пользователя
        user_input = input("\nВаш выбор (название или номер): ").strip().lower()

        # Проверка на выход
        if user_input == 'выход':
            print("\nСпасибо за использование программы! До свидания!")
            break

        # Проверка по номеру
        if user_input.isdigit():
            index = int(user_input) - 1
            keys_list = list(person_info.keys())
            if 0 <= index < len(keys_list):
                key = keys_list[index]
                print(f"\n✓ {key.capitalize()}: {person_info[key]}")
            else:
                print(f"\n✗ Неверный номер. Пожалуйста, выберите от 1 до {len(keys_list)}")

        # Проверка по названию
        elif user_input in person_info:
            print(f"\n✓ {user_input.capitalize()}: {person_info[user_input]}")

        else:
            print(f"\n✗ Информация о '{user_input}' не найдена.")
            print("Попробуйте ещё раз!")


# Запуск интерактивной программы
interactive_search()

