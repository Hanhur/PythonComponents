# 4. Вызовите метод, который превращает строку "Где это? Кто это? Когда это?" в список ["Где это?", "Кто это?", "Когда это?"].

# Исходная строка
text = "Где это? Кто это? Когда это?"

# Разделяем по "? " (вопрос и пробел)
result = text.split("? ")

# Добавляем знак вопроса обратно к каждому элементу (кроме последнего)
result = [item + "?" if i < len(result) - 1 else item for i, item in enumerate(result)]

print(result)

# 5. Превратите список ["Рыжая", "лиса", "перепрыгнула", "через","низкий", "забор", "."] в грамматически правильное предложение.
# Каждое слово должно отделяться пробелом, но между словом «забор» и следующей за ним точкой пробела быть не должно.
# (Не забывайте, вы выучили метод, превращающий список строк в единую строку.)

# Самое элегантное и правильное решение
words = ["Рыжая", "лиса", "перепрыгнула", "через", "низкий", "забор", "."]

# Объединяем все слова через пробел, затем убираем пробел перед точкой
sentence = " ".join(words).replace(" .", ".")

print(sentence)
# Вывод: Рыжая лиса перепрыгнула через низкий забор.

