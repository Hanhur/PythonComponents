# 2. Напишите программу, которая принимает от пользователя две строки,
# вставляет их в строку "Вчера я написал первый_ответ. Вчера я ходил в второй_ответ!" и выводит новую строку.

# Получаем две строки от пользователя
first_answer = input("Введите первое слово (для вставки вместо 'первый_ответ'): ")
second_answer = input("Введите второе слово (для вставки вместо 'второй_ответ'): ")

# Формируем итоговую строку с помощью f-строки
result = f"Вчера я написал {first_answer}. Вчера я ходил в {second_answer}!"

# Выводим результат
print("\nРезультат:")
print(result)

# 3. Используйте метод, чтобы исправить грамматическую ошибку в строке "олдос Хаксли родился в 1894 году.",
