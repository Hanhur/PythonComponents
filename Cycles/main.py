# 4. Напишите программу с бесконечным циклом (с возможностью ввести букву X, чтобы выйти) и списком чисел.
# При каждом переборе цикла предлагайте пользователю отгадать число из списка и сообщайте, правильно ли он отгадал.
# Список чисел для угадывания
numbers = [15, 7, 23, 42, 8, 17]

print("=== Игра 'Угадай число' ===")
print(f"Загаданы числа: {numbers}")
print("Введите 'X' для выхода из игры\n")

# Бесконечный цикл
while True:
    try:
        # Запрашиваем число у пользователя
        user_input = input("Ваше предположение: ")

        # Проверка на выход
        if user_input.upper() == 'X':
            print("Выход из игры. До свидания!")
            break

        # Преобразуем ввод в целое число
        guess = int(user_input)

        # Проверяем, есть ли число в списке
        if guess in numbers:
            print(f"✓ ПРАВИЛЬНО! Число {guess} есть в списке!")
        else:
            print(f"✗ НЕПРАВИЛЬНО! Числа {guess} нет в списке.")

        print("-" * 40)  # Разделитель для наглядности

    except ValueError:
        print("Ошибка! Введите целое число или букву 'X' для выхода.")
        print("-" * 40)

# 5. Умножьте все числа в списке [8, 19, 148, 4] на все числа в списке [9, 1, 33, 83] и поместите результаты в третий список.
list1 = [8, 19, 148, 4]
list2 = [9, 1, 33, 83]
results = []

for num1 in list1:
    for num2 in list2:
        product = num1 * num2
        results.append(product)
        print(f"{num1} × {num2} = {product}")

print(f"\nИтоговый список: {results}")